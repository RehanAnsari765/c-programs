#include <stdio.h>

void main () {

    int day = 7;

    switch (day) {

    case 1:
    printf("SUNDAY");
    break;

    case 2:
    printf("MONDAY");
    break;

    case 3:
    printf("TUESDAY");
    break;

    case 4:
    printf("WEDNESDAY");
    break;

    case 5:
    printf("THURSDAY");
    break;

    case 6:
    printf("FRIDAY");
    break;

    case 7:
    printf("SATDAY");
    break;

    default:
    printf("No Day Found");

    }
}
